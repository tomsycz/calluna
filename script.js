function swapMenu(screenSize) {
  if (screenSize.matches) {
    document
      .querySelector(".swap-action-js")
      .insertAdjacentHTML("afterbegin", menuMobile);
    document
      .querySelector(".menu-mobile")
      .parentElement.removeChild(
        document.querySelector(".swap-action-js").lastElementChild
      );
  } else {
    document
      .querySelector(".swap-action-js")
      .insertAdjacentHTML("afterbegin", menuFull);
    document
      .querySelector(".menu")
      .parentElement.removeChild(
        document.querySelector(".swap-action-js").lastElementChild
      );
  }
}

var screenWidth = window.matchMedia("(max-width: 900px)");
swapMenu(screenWidth); // Call listener function at run time
screenWidth.addListener(swapMenu); // Attach listener function on state changes

